package calculator;

public class Cal {
	public static double add(double x, double y) {
		return x+y;
	}	
	public static double minus(double x, double y) {
		return x - y;
	}
	public static double multi(double x, double y) {
		return x*y;
	}
	public static double divide(double x, double y) {
		return x/y;
	}
}
